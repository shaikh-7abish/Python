stack = []

# append function to push element in stack
stack.append('g')
stack.append('h')
stack.append('i')

print('Initial stack')
print(stack)

# pop function to pop element from stack in LIFO order
print('\n Elements popped from stack: ')
print(stack.pop())
print(stack.pop())
print(stack.pop())

print('\n Elements popped from stack: ')
print(stack)